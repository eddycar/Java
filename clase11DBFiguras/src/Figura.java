public class Figura {

    private String figura;
    private String color;

    public Figura(String figura, String color) {
        this.figura = figura;
        this.color = color;
    }

    public String getFigura() {
        return figura;
    }

    public void setFigura(String figura) {
        this.figura = figura;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
